package escola;

public class Aluno {
    private String nome;
    private int idade;
    private String sexo;

    public String getNome() {
        return nome;
    }

    public void setNome(String newName) {
        nome = newName;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int newIdade) {
        idade = newIdade;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String newSexo) {
        sexo = newSexo;
    }
}
